class Library
{
	public static void main(String[] args)
	{
		double a=1800; 
		double b=40; 
		double c=20; 
		double d=30; 
		double e=10; 
		double f=a*(b/100.0);
		double g=a*(c/100.0);
		double h=a*(d/100.0);
		double i=a*(e/100.0);
		
		System.out.println("Math Books:"+f);
		System.out.println("English Books:"+g);
		System.out.println("Science Books:"+h);
		System.out.println("Social Books:"+i);
	}
}